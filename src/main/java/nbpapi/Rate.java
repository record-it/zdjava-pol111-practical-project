package nbpapi;

public class Rate {
    private java.lang.String currency;
    private java.lang.String code;
    private double mid;
    private double bid;
    private double ask;

    public java.lang.String getCurrency() {
        return currency;
    }

    public java.lang.String getCode() {
        return code;
    }

    public double getMid() {
        return mid;
    }

    public double getBid() {
        return bid;
    }

    public double getAsk() {
        return ask;
    }
}
